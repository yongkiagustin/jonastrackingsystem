package id.yongki.jonastrackingsystem.model;

public class UserModel {
    public  UserModel(String nama, String username, String email, String nowa, String jabatan, String status){
    this.nama = nama;
    this.username = username;
    this.email = email;
    this.nowa = nowa;
    this.jabatan = jabatan;
    this.status = status;
    }

    public String nama;
    public String username;
    public String email;
    public String nowa;
    public String jabatan;
    public String status;
}
